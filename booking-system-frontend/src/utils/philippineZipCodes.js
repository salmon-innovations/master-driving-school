/**
 * Look up the Philippine zip code for an address string.
 *
 * Strategy:
 *  1. Sort all keys by length descending — longer/more-specific names
 *     (e.g. "davao del sur", "bonifacio global city") are tried first.
 *  2. Match the key as a whole word / phrase using word-boundary regex.
 *  3. Return the first (longest) match found, or '' if nothing matches.
 *
 * @param {string} address - The full address string typed by the user.
 * @returns {string} The matched zip code, or '' if nothing matches.
 */
export function getZipFromAddress(address) {
  if (!address) return '';
  const norm = address.toLowerCase().trim();

  const sortedKeys = Object.keys(PH_ZIP_CODES).sort((a, b) => b.length - a.length);

  for (const key of sortedKeys) {
    const escaped = key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    // Word-boundary: key must not be preceded or followed by a letter/digit
    const regex = new RegExp(`(?<![a-záéíóúñ\\d])${escaped}(?![a-záéíóúñ\\d])`, 'i');
    if (regex.test(norm)) {
      return PH_ZIP_CODES[key];
    }
  }

  return '';
}
